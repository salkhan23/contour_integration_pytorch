def get_oo_gains(out_arr, epsilon=1e-4):
    """ gain = output multiple RCD, output rcd=1 (first column) """
    noise_resp_arr = out_arr[:, 0]
    noise_resp_arr = np.expand_dims(noise_resp_arr, axis=1)

    gains = out_arr / (noise_resp_arr + epsilon)
    return gains


def get_filtered_results(result_dict):
    valid_idx_arr = \
        [idx for idx, n_imgs in enumerate(result_dict['n_images']) if n_imgs >= MIN_N_IMGS_PER_CHANNEL]

    filtered_ch_in = result_dict['ch_in'][valid_idx_arr, ]
    filtered_ch_out = result_dict['ch_out'][valid_idx_arr, ]
    filtered_ch_predict = result_dict['ch_predicts'][valid_idx_arr, ]
    filtered_ch_gains = get_oo_gains(filtered_ch_out)

    return filtered_ch_in, filtered_ch_out, filtered_ch_predict, filtered_ch_gains, len(valid_idx_arr)


