def get_combined_and_filtered_results(result_list):
    combined_ch_in = []
    combined_ch_out = []
    combined_ch_predict = []
    combined_ch_gain = []
    total_n = 0

    for r_idx, result_dict in enumerate(result_list):
        ch_in, ch_out, ch_predict, ch_gain, n_ch = get_filtered_results(result_dict)

        total_n += n_ch
        for ch_idx in range(n_ch):
            combined_ch_in.append(ch_in[ch_idx])
            combined_ch_out.append(ch_out[ch_idx])
            combined_ch_predict.append(ch_predict[ch_idx])
            combined_ch_gain.append(ch_gain[ch_idx])

    combined_ch_in = np.array(combined_ch_in)
    combined_ch_out = np.array(combined_ch_out)
    combined_ch_predict = np.array(combined_ch_predict)
    combined_ch_gain = np.array(combined_ch_gain)

    return combined_ch_in, combined_ch_out, combined_ch_predict, combined_ch_gain, total_n


def plot_population_result(ax, rcd, result, name, marker, color):
    means = np.mean(result, axis=0)
    stds = np.std(result, axis=0)
    ax.plot(rcd, means, label='{}'.format(name), color=color, marker=marker)
    ax.fill_between(rcd, means - stds, means + stds, alpha=0.2, color=color)

    return means, stds


def get_gradients_of_linear_fits(x, out_arr):
    out_acts_gradients = []

    for ch_idx in range(len(out_arr)):

        if out_arr[ch_idx, 0] != INVALID_RESULT:
            out_acts = out_arr[ch_idx, ]
            m_out, b_out = np.polyfit(x, out_acts, deg=1)

            out_acts_gradients.append(m_out)

    return np.array(out_acts_gradients)


def plot_histograms(ax, rcd,  ch_in, ch_out, name, color, bins):

    out_grads = get_gradients_of_linear_fits(rcd, ch_out)
    in_grads = get_gradients_of_linear_fits(rcd, ch_in)
    ax.hist(
        out_grads.clip(min=np.min(bins), max=np.max(bins)),  # Include all data points
        label="{}".format(name),
        bins=bins,
        color=color,
        edgecolor='black',
        linewidth=1.2,
    )

    axa = inset_axes(
        ax,
        width="50%",  # width = 50% of parent_bbox
        height=1.,  # height : 1 inch
        loc='center left',
        bbox_to_anchor=(.2, .2, .6, .6),
        bbox_transform=ax.transAxes
    )

    axa.set_title('Inputs')
    axa.hist(
        in_grads.clip(min=np.min(bins), max=np.max(bins)),
        label="In", bins=bins, color=color,
        edgecolor='black', linewidth=1.2
    )

    ax.legend()
    ax.set_xlabel('gradients')
    ax.set_ylabel('Freq')
    ax.set_xticks(np.arange(np.min(bins), np.max(bins) + 1, 5))


