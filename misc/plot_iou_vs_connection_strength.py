def main(results_behave, results_neuro, label, color='black'):

    # -----------------------------------------------------------------------------------
    # Behavioral
    # -----------------------------------------------------------------------------------
    best_train_iou = []
    best_val_iou = []

    best_training_loss = []
    best_val_loss = []

    strengths = []

    for key, value in sorted(results_behave.items()):
        validation_iou_arr = value[:, 4]
        train_iou_arr = value[:, 2]

        validation_loss_arr = value[:, 3]
        train_loss_arr = value[:, 1]

        strengths.append(key)
        best_train_iou.append(np.max(train_iou_arr))
        best_val_iou.append(np.max(validation_iou_arr))
        best_training_loss.append(np.min(train_loss_arr))
        best_val_loss.append(np.min(validation_loss_arr))

    strengths = np.array(strengths)

    # Plot best Iou vs Fixed conection strength
    plt.figure("IoU", figsize=(9, 9))
    plt.plot(sigmoid(strengths), best_train_iou, label='train',
             marker='x', markersize=10, markeredgewidth=3, color=color)
    plt.plot(sigmoid(strengths), best_val_iou, label='val',
             marker='x', markersize=10, color=color, linestyle='--', markeredgewidth=3)
    plt.xlabel(r"$ Connection \/ Strength \/ \sigma({})$".format(label))
    plt.ylabel("IoU")
    # plt.title("Iou vs Rf Size")
    plt.legend()
    plt.ylim([0, 1])
    # plt.grid(True)
    plt.tight_layout()

    # Plot lowest loss vs Fixed connection strength
    plt.figure("Loss", figsize=(9, 9))
    plt.plot(sigmoid(strengths), best_training_loss, label='train' + label,
             marker='x', markersize=10, color=color, markeredgewidth=3)
    plt.plot(sigmoid(strengths), best_val_loss, label='val' + label,
             marker='x', markersize=10, linestyle='--', markeredgewidth=3, color=color)
    plt.xlabel(r"$ Connection \/ Strength \/ \sigma({})$".format(label))
    plt.ylabel("Loss")
    # plt.title("Loss vs RF Size")
    plt.legend()
    # plt.grid(True)
    plt.tight_layout()

    # -----------------------------------------------------------------------------------
    # Neuro
    # -----------------------------------------------------------------------------------
    plt.figure("Clen Gain Curves", figsize=(9, 9))
    c_len_arr = np.array([1, 3, 5, 7, 9])

    for key, value in sorted(results_neuro.items()):

        valid_idx_arr = fig_exp1_results_thesis.get_filtered_idx_arr_max_gain(value)

