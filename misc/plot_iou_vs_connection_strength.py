def plot_behavioral_loss(results_behave, label, color='black', axis=None):
    if axis is None:
        plt.figure("Loss", figsize=(9, 9))

    lowest_train_loss = []
    lowest_val_loss = []

    strengths = []

    for key, value in sorted(results_behave.items()):
        validation_iou_arr = value[:, 4]
        train_iou_arr = value[:, 2]

        strengths.append(key)
        lowest_train_loss.append(np.max(train_iou_arr))
        lowest_val_loss.append(np.max(validation_iou_arr))

    strengths = np.array(strengths)

    axis.plot(
        sigmoid(strengths), lowest_train_loss, label='train ' + r"$\sigma({})$".format(label),
        marker='x', markersize=10, markeredgewidth=3, color=color)
    axis.plot(
        sigmoid(strengths), lowest_val_loss, label='val ' + r"$\sigma({})$".format(label),
        marker='x', markersize=10, color=color, linestyle='--', markeredgewidth=3)

    axis.set_xlabel("Connection Strength".format(label))
    axis.set_ylabel("Loss")
    axis.legend()
    # axis.grid(True)
    # axis.set_title("Iou vs Rf Size")


