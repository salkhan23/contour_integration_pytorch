def plot_curve(axis, time, mu, sigma, label=None, color='k', linestyle='-'):
    axis.plot(time, mu, label=label, color=color, linestyle=linestyle)
    axis.fill_between(
        time,
        mu - sigma,
        mu + sigma,
        alpha=0.2,
        color=color,
    )


