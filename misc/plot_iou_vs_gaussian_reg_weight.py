def main(results, label, color='black'):
    best_train_iou = []
    best_val_iou = []

    best_training_loss = []
    best_val_loss = []

    rf_size_arr = []

    for key, value in sorted(results.items()):
        validation_iou_arr = value[:, 4]
        train_iou_arr = value[:, 2]

        validation_loss_arr = value[:, 3]
        train_loss_arr = value[:, 1]

        rf_size_arr.append(key)
        best_train_iou.append(np.max(train_iou_arr))
        best_val_iou.append(np.max(validation_iou_arr))
        best_training_loss.append(np.min(train_loss_arr))
        best_val_loss.append(np.min(validation_loss_arr))

    # Plot best Iou vs Loss Weight
    plt.figure("IoU", figsize=(6, 6))
    plt.plot(
        rf_size_arr, best_train_iou, label=label + ' train',
        marker='x', markersize=10, markeredgewidth=3, color=color)
    plt.plot(
        rf_size_arr, best_val_iou, label=label + ' val ',
        marker='x', markersize=10,  markeredgewidth=3, color=color, linestyle='--')
    plt.xlabel("Weight")
    plt.xscale('log')
    plt.ylim([0, 1])
    plt.ylabel("IoU")
    # plt.title("Iou vs Regularization Weight")
    plt.legend()
    # plt.grid(True)
    plt.tight_layout()

    # Plot lowest loss vs Tau
    plt.figure("Loss")
    plt.plot(
        rf_size_arr, best_training_loss, label=label + ' train',
        marker='x', markersize=10, markeredgewidth=3, color=color)
    plt.plot(
        rf_size_arr, best_val_loss, label=label + ' val',
        marker='x', markersize=10,  markeredgewidth=3, color=color, linestyle='--')
    plt.xlabel("Weight")
    plt.xscale('log')
    plt.ylabel("Loss")
    # plt.title("Loss vs Regularization Weight")
    plt.legend()
    # plt.grid(True)
    plt.tight_layout()

    # Plot Individual Loss/Iou Curves
    num_keys = len(results.keys())
    single_dim = np.int(np.ceil(np.sqrt(num_keys)))

    fig1 = plt.figure()
    fig2 = plt.figure()

    x_label = 'weight'

    for k_idx, key in enumerate(sorted(results.keys())):
        ax1 = fig1.add_subplot(single_dim, single_dim, k_idx + 1)
        ax2 = fig2.add_subplot(single_dim, single_dim, k_idx + 1)

        ax1.plot(
            results[key][:, 0],
            results[key][:, 2],
            label='train_iou')

        ax1.plot(
            results[key][:, 0],
            results[key][:, 4],
            label='val_iou')

        ax1.set_title(x_label + " {}".format(key))

        ax2.plot(
            results[key][:, 0],
            results[key][:, 1],
            label='train_loss')

        ax2.plot(
            results[key][:, 0],
            results[key][:, 3],
            label='val_iou_loss')

        # ax2.set_yscale('log')

        ax1.set_title(x_label + " = {}".format(key))
        ax1.grid()
        ax2.set_title(x_label + " ={}".format(key))
        ax2.grid()
        ax1.legend()
        ax2.legend()

    fig1.suptitle("{} - Iou Vs Regularization Weight - Individual Curves".format(label))
    fig2.suptitle("{} - Loss Vs Regularization Weight - Individual Curves".format(label))


